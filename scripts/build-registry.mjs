// Scan loaders/ -> generate data/registry.json + data/registry.md.
// Source of truth is the per-loader folders; registry files are always regenerated.
import { mkdir, writeFile } from 'node:fs/promises'
import { join } from 'node:path'
import {
  ROOT, DATA_DIR, findMetaFiles, readLoader, cssHash,
} from './lib.mjs'

const TARGET = 1000

function pad(s, n) {
  s = String(s)
  return s + ' '.repeat(Math.max(0, n - s.length))
}

async function main() {
  const metaFiles = await findMetaFiles()
  const items = []
  const seenIds = new Map()
  const seenCss = new Map()
  const warnings = []

  for (const mp of metaFiles) {
    let loaded
    try {
      loaded = await readLoader(mp)
    } catch (e) {
      warnings.push(`Cannot parse ${mp}: ${e.message}`)
      continue
    }
    const { item } = loaded

    if (seenIds.has(item.id)) {
      warnings.push(`Duplicate id "${item.id}" (also ${seenIds.get(item.id)})`)
      continue
    }
    seenIds.set(item.id, mp)

    // Generated variants intentionally share base CSS (they differ via CSS
    // variables), so only dedup-check hand-authored / imported loaders.
    if (item.css && !item.generated) {
      const h = cssHash(item.css)
      if (seenCss.has(h)) {
        warnings.push(`Possible duplicate CSS: "${item.id}" matches "${seenCss.get(h)}"`)
      } else {
        seenCss.set(h, item.id)
      }
    }
    items.push(item)
  }

  // Stable sort: category, then id.
  items.sort((a, b) =>
    a.category === b.category ? a.id.localeCompare(b.id) : a.category.localeCompare(b.category))

  // Indexes.
  const categories = {}
  const breakdown = {} // { category: { original, generated } }
  const tagSet = new Set()
  let generatedCount = 0
  for (const it of items) {
    categories[it.category] = (categories[it.category] ?? 0) + 1
    breakdown[it.category] ??= { original: 0, generated: 0 }
    if (it.generated) { breakdown[it.category].generated++; generatedCount++ }
    else breakdown[it.category].original++
    for (const t of it.tags) tagSet.add(t)
  }

  const registry = {
    $schema: './../schema/loader.schema.json',
    name: 'loading-companion',
    version: '0.1.0',
    generator: 'scripts/build-registry.mjs',
    count: items.length,
    target: TARGET,
    categories,
    tags: [...tagSet].sort(),
    items,
  }

  await mkdir(DATA_DIR, { recursive: true })
  await writeFile(join(DATA_DIR, 'registry.json'), JSON.stringify(registry, null, 2) + '\n')
  await writeFile(join(DATA_DIR, 'registry.md'), renderMarkdown(registry))

  // Also emit a minified copy under public/ so the gallery can fetch it at
  // runtime (kept out of the JS bundle). data/registry.json stays the canonical,
  // committed source consumed by the MCP server and AI agents.
  const PUBLIC_DIR = join(ROOT, 'public')
  await mkdir(PUBLIC_DIR, { recursive: true })
  await writeFile(join(PUBLIC_DIR, 'registry.json'), JSON.stringify(registry))

  // Report.
  console.log('\n=== Loading Companion — registry build ===')
  console.log(`Total loaders: ${items.length} / ${TARGET} target`)
  console.log(`  original: ${items.length - generatedCount}   generated: ${generatedCount}`)
  console.log('\nBreakdown by category (original + generated):')
  for (const cat of Object.keys(breakdown).sort()) {
    const b = breakdown[cat]
    console.log(`  ${pad(cat, 12)} ${pad(b.original + b.generated, 5)} (${b.original} orig + ${b.generated} gen)`)
  }
  console.log(`\nUnique tags: ${registry.tags.length}`)

  if (warnings.length) {
    console.log(`\n⚠️  ${warnings.length} warning(s):`)
    for (const w of warnings) console.log('  - ' + w)
  }

  if (items.length < TARGET) {
    console.log(`\nℹ️  ${TARGET - items.length} loaders to go to reach the ${TARGET} target.`)
  } else {
    console.log(`\n✅ Target of ${TARGET} reached.`)
  }
  console.log('Wrote data/registry.json and data/registry.md\n')
}

function renderMarkdown(reg) {
  const lines = []
  lines.push(`# Loading Companion — Catalog`)
  lines.push('')
  lines.push(`> Auto-generated by \`scripts/build-registry.mjs\`. Do not edit by hand.`)
  lines.push('')
  lines.push(`**${reg.count}** loaders across **${Object.keys(reg.categories).length}** categories (target ${reg.target}).`)
  lines.push('')
  lines.push('| Category | Count |')
  lines.push('| --- | --- |')
  for (const cat of Object.keys(reg.categories).sort()) {
    lines.push(`| ${cat} | ${reg.categories[cat]} |`)
  }
  lines.push('')
  let lastCat = null
  for (const it of reg.items) {
    if (it.category !== lastCat) {
      lines.push(`\n## ${it.category}\n`)
      lastCat = it.category
    }
    lines.push(`### \`${it.id}\` — ${it.name}`)
    const meta = [`tech: ${it.tech}`, `tags: ${it.tags.join(', ') || '—'}`, `license: ${it.license}`]
    if (it.generated) meta.push(`generated from: ${it.baseId}`)
    if (it.source?.name) meta.push(`source: ${it.source.name}`)
    lines.push(`_${meta.join(' · ')}_`)
    lines.push('')
    if (it.html) { lines.push('```html'); lines.push(it.html); lines.push('```') }
    if (it.css) { lines.push('```css'); lines.push(it.css); lines.push('```') }
    if (it.js) { lines.push('```js'); lines.push(it.js); lines.push('```') }
    lines.push('')
  }
  return lines.join('\n')
}

main().catch((e) => { console.error(e); process.exit(1) })
